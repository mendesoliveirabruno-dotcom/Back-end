n = input('digite algo')
print(n.isnumeric())# para perguntar se é um número
print(n.isalpha())# para perguntar se é letra
print(n.isalnum())# para perguntar se é letra ou número
print(n.isupper())# para perguntar se é so com letras maisculas
print(n.isdecimal())#para verificar se é decimal
print(n.isdigit())#para veriicar se é um digito
print(n.islower())#para verificar se é apenas letras minussculas
print(n.isidentifier())#para veiicar se é um identificador valido
print(n.isspace)#para verificar se tem espaço branco
